let currentLang = localStorage.getItem('voteready_lang') || 'en';

function translateTextNode(node) {
    if (currentLang === 'en') return; // For English, we rely on the original HTML/JSON (this simplistic approach works best for one-way or requires storing original text)
    // Actually, if we want to toggle back, we should store original text on the node.
    if (node.nodeType === Node.TEXT_NODE && node.nodeValue.trim() !== '') {
        if (!node.originalText) {
            node.originalText = node.nodeValue;
        }

        let textToMatch = node.originalText.trim();
        if (dictionary[textToMatch]) {
            node.nodeValue = node.nodeValue.replace(textToMatch, dictionary[textToMatch]);
        }
    }
}

function translateElement(el) {
    if (currentLang === 'en') {
        // Restore
        const walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT, null, false);
        let node;
        while (node = walker.nextNode()) {
            if (node.originalText) {
                node.nodeValue = node.originalText;
            }
        }
        if (el.placeholder && el.originalPlaceholder) {
            el.placeholder = el.originalPlaceholder;
        }
    } else {
        const walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT, null, false);
        let node;
        while (node = walker.nextNode()) {
            // skip script and style tags
            if (node.parentElement && (node.parentElement.tagName === 'SCRIPT' || node.parentElement.tagName === 'STYLE')) continue;
            translateTextNode(node);
        }
        if (el.placeholder) {
            if (!el.originalPlaceholder) el.originalPlaceholder = el.placeholder;
            if (dictionary[el.originalPlaceholder]) {
                el.placeholder = dictionary[el.originalPlaceholder];
            }
        }
        // translate specific attributes if needed
        el.querySelectorAll && el.querySelectorAll('input[placeholder]').forEach(input => {
            if (!input.originalPlaceholder) input.originalPlaceholder = input.placeholder;
            if (dictionary[input.originalPlaceholder]) input.placeholder = dictionary[input.originalPlaceholder];
        });
    }
}

function applyTranslation() {
    translateElement(document.body);
    translateDataKeys();
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        toggleBtn.textContent = currentLang === 'en' ? 'अ/A' : 'A/अ';
    }
}

// Translates elements that carry a data-key attribute (used in dynamically injected HTML)
function translateDataKeys(root) {
    (root || document).querySelectorAll('[data-key]').forEach(el => {
        el.textContent = (currentLang === 'hi' && dictionary[el.dataset.key])
            ? dictionary[el.dataset.key]
            : el.dataset.key;
    });
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'hi' : 'en';
    localStorage.setItem('voteready_lang', currentLang);
    applyTranslation();
}

// Observe dynamic changes (for the checklist JSON rendering or AI responses)
const observer = new MutationObserver((mutations) => {
    if (currentLang === 'hi') {
        mutations.forEach(mutation => {
            mutation.addedNodes.forEach(node => {
                if (node.nodeType === Node.ELEMENT_NODE) {
                    translateElement(node);
                    translateDataKeys(node);
                } else if (node.nodeType === Node.TEXT_NODE) {
                    translateTextNode(node);
                }
            });
        });
    }
});

